// Declaring variables of different data types

#include <stdio.h>

int main() {
    int num = 10;
    float pi = 3.14;
    char grade = 'A';
    double largeNumber = 12345.6789;
    printf("Integer: %d, Float: %.2f, Char: %c, Double: %.4lf\n", num, pi, grade, largeNumber);
    return 0;
}
