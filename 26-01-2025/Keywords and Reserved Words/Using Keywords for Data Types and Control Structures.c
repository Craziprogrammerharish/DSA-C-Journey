#include <stdio.h>

int main() {
    int num = 10;             // 'int' is a keyword for integer type
    float pi = 3.14;          // 'float' is a keyword for floating-point numbers

    if (num > 5) {            // 'if' is a control statement keyword
        printf("Number %d is greater than 5\n", num);
    } else {                  // 'else' is also a control statement keyword
        printf("Number %d is not greater than 5\n", num);
    }

    return 0;                 // 'return' is a keyword to return control to OS
}
